package com.github.ltsopensource.client.jdl;

/**
 * Represent join info of {@link ForkObject}
 */
public class JoinObject {
    private String name;
    private String to;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTo() {
        return to;
    }

    public void setTo(String to) {
        this.to = to;
    }
}
